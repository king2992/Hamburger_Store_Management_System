package kr.ac.kopo.model;

public class MenuInfo {
	int menuId;
	String menuIntroduce;
	String menuKcal;
	String menuName;
	String menuImg;
	
	
	public String getMenuImg() {
		return menuImg;
	}
	public void setMenuImg(String menuImg) {
		this.menuImg = menuImg;
	}
	public int getMenuId() {
		return menuId;
	}
	public void setMenuId(int menuId) {
		this.menuId = menuId;
	}
	public String getMenuIntroduce() {
		return menuIntroduce;
	}
	public void setMenuIntroduce(String menuIntroduce) {
		this.menuIntroduce = menuIntroduce;
	}
	public String getMenuKcal() {
		return menuKcal;
	}
	public void setMenuKcal(String menuKcal) {
		this.menuKcal = menuKcal;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
}
